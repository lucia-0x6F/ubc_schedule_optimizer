// color palette for courses (same course -> same color)
const COURSE_COLORS = [
  "#bfdbfe", // blue
  "#fecaca", // red
  "#bbf7d0", // green
  "#fde68a", // yellow
  "#f5d0fe", // purple
  "#fed7aa", // orange
  "#a5f3fc", // teal
];

function getColorForCourse(courseName) {
  let hash = 0;
  for (let i = 0; i < courseName.length; i++) {
    hash = (hash * 31 + courseName.charCodeAt(i)) | 0;
  }
  const index = Math.abs(hash) % COURSE_COLORS.length;
  return COURSE_COLORS[index];
}

export default function CourseSearch() {
  const [query, setQuery] = useState("");
  const [selectedCourses, setSelectedCourses] = useState([]);
  const [hovered, setHovered] = useState(null);

  // schedules generated by the algo (each item is one schedule = array of sections)
  const [generatedSchedules, setGeneratedSchedules] = useState([]);

  const filteredCourses = UBC_COURSES.filter((course) =>
    course.toLowerCase().includes(query.toLowerCase())
  );

  const handleSelectCourse = (course) => {
    if (!selectedCourses.includes(course)) {
      setSelectedCourses([...selectedCourses, course]);
    }
    setQuery("");
  };

  const handleRemoveCourse = (courseToRemove) => {
    const newSelected = selectedCourses.filter((c) => c !== courseToRemove);
    setSelectedCourses(newSelected);

    // optional: if removed everything, clear schedules too
    if (newSelected.length === 0) {
      setGeneratedSchedules([]);
    }
  };

  // üî• Use your partner's optimizeSchedules wrapper here
  const handleGenerateSchedule = () => {
    if (selectedCourses.length === 0) {
      setGeneratedSchedules([]);
      return;
    }

    // For now, no extra filters; you can plug real options later
    const options = {
      // noClassDays: ["Fri"],
      // minStart: "09:00",
      // maxEnd: "18:00",
      // preferredTimes: { ... }
    };

    const results = optimizeSchedules(selectedCourses, options);
    setGeneratedSchedules(results); // array of schedules ‚Üí ScheduleGrid
  };

  return (
    <div style={styles.page}>
      {/* LEFT COLUMN */}
      <div style={styles.leftColumn}>
        {/* WHITE CARD that wraps everything */}
        <div style={styles.card}>
          {/* SEARCH AREA (with overlay dropdown) */}
          <div style={styles.searchArea}>
            <div style={styles.searchWrapper}>
              <span style={styles.icon}>üîç</span>
              <input
                type="text"
                placeholder="Search UBC courses (e.g. CPSC110)"
                value={query}
                onChange={(e) => setQuery(e.target.value)}
                style={styles.input}
              />
            </div>

            {query !== "" && (
              <ul style={styles.resultsList}>
                {filteredCourses.length > 0 ? (
                  filteredCourses.map((course) => (
                    <li
                      key={course}
                      style={styles.resultItem}
                      onClick={() => handleSelectCourse(course)}
                    >
                      {course}
                    </li>
                  ))
                ) : (
                  <li style={styles.noResult}>No matching course found</li>
                )}
              </ul>
            )}
          </div>

          {/* SELECTED COURSES */}
          {selectedCourses.length > 0 && (
            <div style={styles.selectedWrapper}>
              <div style={styles.selectedTitle}>Selected courses</div>
              <div style={styles.selectedList}>
                {selectedCourses.map((course) => {
                  const color = getColorForCourse(course);
                  return (
                    <div
                      key={course}
                      style={{
                        ...styles.selectedChip,
                        backgroundColor: color,
                      }}
                    >
                      <span>{course}</span>
                      <button
                        type="button"
                        style={{
                          ...styles.deleteButton,
                          color: hovered === course ? "#b91c1c" : "#111827",
                          fontWeight: hovered === course ? 700 : 500,
                        }}
                        onMouseEnter={() => setHovered(course)}
                        onMouseLeave={() => setHovered(null)}
                        onClick={() => handleRemoveCourse(course)}
                      >
                        √ó
                      </button>
                    </div>
                  );
                })}
              </div>
            </div>
          )}

          {/* GENERATE BUTTON */}
          <div style={styles.generateButtonWrapper}>
            <button
              onClick={handleGenerateSchedule}
              style={{
                ...styles.generateButton,
                opacity: selectedCourses.length === 0 ? 0.5 : 1,
                cursor: selectedCourses.length === 0 ? "not-allowed" : "pointer",
              }}
              disabled={selectedCourses.length === 0}
            >
              Generate schedule
            </button>
          </div>
        </div>
      </div>

      {/* RIGHT COLUMN: SCHEDULE GRID */}
      <div style={styles.rightColumn}>
        <ScheduleGrid
          schedules={generatedSchedules}
          getCourseColor={getColorForCourse} // so blocks match chips
        />
      </div>
    </div>
  );
}

